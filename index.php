<h1>Cruz Vermelha Brasileira Filial de Queimados/RJ</h1>
<p class="postadoem">Postado em 20/08/2016</p>
<p>A Cruz Vermelha Brasileira – Filial do Município de Queimados, inaugurada em 9 de fevereiro de 2011, é uma associação de socorro voluntário, auxiliar dos poderes públicos e, em particular, dos serviços militares de saúde, tem personalidade jurídica independente e sem fins lucrativos, de natureza filantrópica, constituída com base nas Convenções de Genebra, tem como princípios fundamentais: Humanidade, Imparcialidade, Neutralidade, Independência, Voluntariado, Unidade e Universalidade.
Tem, por finalidade, prevenir e atenuar os sofrimentos humanos com toda a imparcialidade, sem distinção de raça, nacionalidade, sexo, nível social, religião e opinião política, podendo sua atuação, em determinados casos, estender-se além do território nacional. Sua missão compreende:
</p>
<p>
<ul>
<li>I.	Agir, em caso de guerra, e preparar-se, na paz, para atuar em todos os setores abrangidos pelas Convenções de Genebra e em favor de todas as vítimas de guerra, tanto civis como militares;</li>
<li>II.	Contribuir para a melhoria de saúde, a prevenção de doenças e o alívio do sofrimento, através de programas de treinamento e de serviços que beneficiem a comunidade, adaptados às necessidades de peculiaridades nacionais e regionais, podendo também, para isso, criar e manter cursos regulares, profissionalizantes e de nível superior; </li>
<li>III.	Organizar, dentro do plano municipal, serviços de socorro de emergência às vítimas de calamidade, seja qual for sua causa;</li>
<li>IV.	Recrutar, treinar e aplicar o pessoal necessário às finalidades da instituição;</li>
<li>V.	Incentivar a participação da comunidade em geral, especialmente crianças e jovens, nas atividades da instituição;</li>
<li>VI.	Divulgar os princípios humanitários da Cruz Vermelha a fim de desenvolver na população os ideais de paz, respeito mútuo e compreensão entre todos os homens e todos os povos.</li></p>
</ul>
</p>

