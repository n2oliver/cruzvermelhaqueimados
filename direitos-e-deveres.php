<h1>Direitos e deveres do voluntário na Cruz Vermelha</h1>
<p class="postadoem">Postado em 20/08/2016</p>
<h4><b>Direitos:</b></h4>
<p >
<span class="destaque">“A ação voluntária oferece motivação e oportunidade de crescimento aos voluntários.”</span>

<ul class="no-bull">
<li>Reconhecimento e respeito dos direitos consignados nos estatutos e regulamentos da Instituição.
</li>
<li>Realizar uma ação voluntária de acordo com as suas capacidades, aptidões e interesses.
</li>
<li>Respeitar o compromisso adquirido com a Cruz Vermelha. Ser informado sobre os Princípios Fundamentais da Cruz Vermelha, Convenções de Genebra, objetivos, estrutura, funcionamento e das tarefas que pode realizar.
</li>
<li>Ser-lhe dada uma tarefa específica e bem definida.
</li>
<li>Ser reconhecido pelo trabalho que desenvolve com acreditação e certificação. Receber treino para a execução da tarefa.
</li>
<li>Receber formação inicial e contínua.
</li>
<li>Estar informado sobre os objetivos, duração e lugar das atividades que vai realizar.
</li>
<li>Fazer avaliações regulares do seu desempenho.
</li>
</ul>
</p>
<h4><b>Deveres:</b></h4>
<p >
<span class="destaque">“O voluntário da Cruz Vermelha Brasileira é assíduo e regular nos compromissos assumidos.”</span>
<ul class="no-bull">
<li>Agir de acordo com os Princípios Fundamentais do Movimento Internacional da Cruz Vermelha e do Crescente Vermelho e promover a sua difusão.</li>
<li>Respeitar as regras relativas ao uso do emblema e impedir o seu abuso.
</li>
<li>Empenhar-se em oferecer os melhores serviços possíveis.</li>
<li>Desempenhar as missões sem discriminação alguma quanto à nacionalidade, raça, sexo, opiniões políticas ou crenças religiosas.</li>
<li>Respeitar o indivíduo.</li>
<li>Respeitar o desejo de descrição daqueles a quem se oferece ajuda.</li>
<li>Promover a compreensão mútua.</li>
<li>Responder às necessidades de outrem com humanidade e simpatia.</li>
<li>Funcionar em equipe.</li>
<li>Formar-se para as atividades e funções que lhe são confiadas.</li>
<li>Familiarizar-se com o Código de Deontologia, os Princípios Fundamentais, a filosofia do Voluntariado Cruz Vermelha / Crescente Vermelho, as quatro Convenções de Genebra e os seus Protocolos Adicionais.</li>
<li>Em caso de dúvida sobre a posição da Cruz Vermelha ou Crescente Vermelho, pedir um conselho, antes de agir, à sua Sociedade Nacional.</li>
<li>Ser capaz de transmitir a informação recebida ou o conhecimento adquirido, e de avaliar o trabalho desenvolvido.</li>
<li>Participar ativamente na Instituição conforme estipulado nos estatutos e regulamentos.</li>
</ul>
</p>
