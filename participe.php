<h1>Seja você também um de nossos voluntários</h1>
<p>
O objetivo do voluntário é de ajudar o próximo proporcionando momentos interessantes e felizes. Quando você ajuda uma pessoa que está em entidades assistenciais, isso é tão gratificante para quem se ajuda e para si mesmo. Ao mesmo tempo em que é gratificante, podemos elevar a autoestima, diminuir a ansiedade e até mesmo prevenir doenças, como a depressão e o estresse. Com o trabalho voluntário, todos nós aprendemos e compartilhamos novas experiências, com palavras ou com um simples gesto.
</p>
<h3>O voluntário na Cruz Vermelha</h3>
<p>Voluntário é aquele que coopera com a Instituição de maneiras diversas, sem buscar o lucro ou recompensa, mas está sempre satisfeito em ajudar a comunidade e trazer benefícios ao seu próximo.</p>
<p>Sendo assim, a Cruz Vernelha convida pessoas a assumirem uma posição de suma importância, transversal, apoiando projetos e ações que desenvolvem a diferentes níveis da Cruz Vermelha.</p>
<p><span class="texto-cinza">
"Não seria possível formar sociedades de auxílio que cuidassem dos feridos em tempo de guerra, através do trabalho zeloso, devoto e qualificado de voluntários?”</span>, Uma Recordação de Solferino, Henry Dunant - Fundador da Cruz Vermelha,1862</p>
<p>Henry Dunant, há quase 150 anos, usou essas palavras expressando a necessidade  daqueles que saram as nossas feridas. Este homem contribui para o crescimento do Movimento da Cruz Vermelha e do Crescente Vermelho. Hoje, quase 100 milhões de voluntários atuam através da Cruz Vermelha e do Crescente Vermelho no mundo inteiro. Por missão, trabalham todos num só espírito de humanidade, ajuda mútua e solidariedade.</p> 
